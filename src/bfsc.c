#include <stdio.h>

#include "parser.h"

void
print_prog(struct op_s *prog) {
    int i;
    while(prog) {
	if(prog->opcode == '[') {
	    printf("[");
	    print_prog(prog->loop);
	    printf("]");
	} else
	    for(i=0;i<prog->val;i++)
		printf("%c", prog->opcode);
	prog = prog->next;
    }
}

int
main(int argc, char **argv) {
    struct op_s *prog;

    /* ensure a valid program filename */
    if(argc<2 || (prog = parse (argv[1])) == NULL) {
	printf("Usage\n\t%s <program.bf>\n", *argv);
	return -1;
    }

    print_prog(prog);
    printf("\n");

    return 0;
}
